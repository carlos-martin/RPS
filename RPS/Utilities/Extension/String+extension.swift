extension String {
    struct Game {
        static var noMovement: String = "No movement"
        static var waitingForYou: String = "Waiting for you"
    }

    struct Player {
        static var noName: String = "No player"
        static var one: String = "Player 1"
        static var two: String = "Player 2"
    }
}
